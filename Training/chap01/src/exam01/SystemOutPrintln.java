package exam01;

public class SystemOutPrintln {

	public static void main(String[] args) {

		System.out.println("Hello Java!");

		System.out.println(100);
		System.out.println(3.14);

		System.out.println(100 + 15);
		System.out.println(100 - 15);
		System.out.println(100 * 15);
		System.out.println(100 / 15);
		
		System.out.println("Hello" + "Java!");
		
		System.out.println("100" + 100);
		

	}

}
