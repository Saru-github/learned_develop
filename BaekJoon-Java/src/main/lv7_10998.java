package main;

import java.util.Scanner;

public class lv7_10998 {

	public static void main(String[] args) {

//		입력
//		첫째 줄에 A와 B가 주어진다. (0 < A, B < 10)
//
//		출력
//		첫째 줄에 A×B를 출력한다.
		
		
		
		Scanner sc = new Scanner(System.in);
		int a = sc.nextInt();
		int b = sc.nextInt();
		System.out.println(a*b);
		
		sc.close();
		
	}

}
